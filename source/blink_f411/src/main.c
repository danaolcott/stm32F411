#include <stddef.h>
#include <stdint.h>
#include <string.h>

#include "main.h"




///////////////////////////////////////////
//globals
volatile uint32_t TimingDelay;

char buffer[64] = {0x00};
int size = 0;

int main(void)
{

    //Configure the systick with interrupts, HSI = 16Mhz
    if (SysTick_Config(SystemCoreClock / 1000))
    {
        while (1);
    }

    //configure the hardware
    gpio_init();
    gpio_button_init();
    adc_init();         //pa1 - adc1 ch1 - need to configure PA1 as alternate function
    usart2_init();
    spi1_init();

    LCD_Config();
    LCD_BacklightOn();
    LCD_Clear(0x00);

    //returns the status of the sdcard
    //value > 0 - Size of the directory file after it's built
    //value < 0 - negative value of the FR_RESULT code

    LCD_DrawStringKern(0, 3, "Init SDCard...");
    int result = SD_Init();

    if (result > 0)
    {
        size = snprintf(buffer, 32, "SD Res: %d", result);
        LCD_DrawStringKernLength(1, 3, (uint8_t*)buffer, size);
        size = snprintf(buffer, 32, "Dir Size: %d", result);
        LCD_DrawStringKernLength(2, 3, (uint8_t*)buffer, size);
    }
    else
    {
        //invalid / problem with initializing the sdcard
        LCD_DrawStringKern(1, 3, "SDCard Error...");
        size = snprintf(buffer, 32, "SD Res: %d", (-1)*result);
        LCD_DrawStringKernLength(2, 3, (uint8_t*)buffer, size);
    }

    LCD_DrawStringKern(4, 3, "Press Btn ...");

    while (!buttonFlag)
    {

    }

    buttonFlag = 0;

    //make a file
    SD_FileCreate("counter.txt");
    char* msg = "This is the header in the counter file\r\n";
    SD_AppendData("counter.txt", msg, strlen(msg));

    /* Infinite loop */
    while (1)
    {
        LCD_Clear(0x00);
        LCD_DrawStringKern(0, 3, "Hell0 LCD");

        //output the adc from the dma buffer and the register directly
        size = snprintf(buffer, 32, "ADC: %d", (uint16_t)adc_getValueFromDMA());
        LCD_DrawStringKernLength(2, 3, (uint8_t*)buffer, size);

        size = snprintf(buffer, 32, "ADC-DR: %d", (uint16_t)ADC1->DR);
        LCD_DrawStringKernLength(3, 3, (uint8_t*)buffer, size);

        //read the buttonFlag
        if (buttonFlag == 1)
        {
            //do something
            buttonFlag = 0;
        }

        gpio_shieldLedToggle();

        Delay(1000);

    }
}



/////////////////////////////////////////////////
//Delay
void Delay(volatile uint32_t timeTick)
{
    TimingDelay = timeTick;
    while (TimingDelay != 0);
}


////////////////////////////////////////////////
void TimingDelay_Decrement(void)
{
    if (TimingDelay != 0)
        TimingDelay--;
}



