//globals
volatile uint32_t TimingDelay;


int main(void)
{

    //Configure the systick with interrupts, HSI = 16Mhz
    if (SysTick_Config(SystemCoreClock / 1000))
    {
        while (1);
    }

    //configure the hardware
    gpio_init();
    gpio_button_init();

    usart2_init();
    spi1_init();

    LCD_Config();
    LCD_BacklightOn();
    LCD_Clear(0x00);



    /* Infinite loop */
    while (1)
    {
        LCD_Clear(0x00);
        LCD_DrawStringKern(0, 3, "HELLO");
        LCD_DrawStringKern(1, 3, "This is a");
        LCD_DrawStringKern(2, 3, "New Line");
        LCD_DrawStringKern(3, 3, "HELLO");
        LCD_DrawStringKern(4, 3, "This is a");
        LCD_DrawStringKern(5, 3, "New Line");
        LCD_DrawStringKern(6, 3, "New Line");
        LCD_DrawStringKern(7, 3, "New Line");

        //read the buttonFlag
        if (buttonFlag == 1)
        {
            usart2_txString("Hello from program\r\n");

            buttonFlag = 0;
        }

        Delay(500);
//        GPIO_ToggleBits(GPIOA, GPIO_Pin_5);


    }
}



/////////////////////////////////////////////////
//Delay
void Delay(volatile uint32_t timeTick)
{
    TimingDelay = timeTick;
    while (TimingDelay != 0);
}


////////////////////////////////////////////////
void TimingDelay_Decrement(void)
{
    if (TimingDelay != 0)
        TimingDelay--;
}



